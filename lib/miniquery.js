class SweetSelector {
  static select(param) {
    if(param.length > 1){
      var selector = param.split("")[0]
      param = param.slice(1)
    }

    if(selector === "#"){
      var id = document.getElementById(`${param}`)
      console.log(id);
    }else if(selector === '.'){
      var klass = document.getElementsByClassName(`${param}`)
      console.log(klass);
    }else{
      var tagName = document.getElementsByTagName(`${param}`)[0].outerHTML
      console.log(tagName);
    }
    // return true
  }
}

